package chapter02;

public class IntegerLiteralExample {
	public static void main(String[] args) {
//		0b 이진수
		int var1=0b1011;
		int var2=0206;
		int var3=365;
		int var4=0xB3;
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);
		System.out.println(var4);
	}
}
